/** Look up an enemy by id; throws on unknown id. */
export function enemyById(id: string): EnemyDef {
  const def = ENEMIES.find((e) => e.id === id);
  if (!def) throw new Error(`Unknown enemy id: ${id}`);
  return def;
}

/** Look up a boss by id; throws on unknown id. */
export function bossById(id: string): BossDef {
  const def = BOSSES.find((b) => b.id === id);
  if (!def) throw new Error(`Unknown boss id: ${id}`);
  return def;
}
